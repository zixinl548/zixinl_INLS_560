# Experimenting with expressions

my_age = 20
operand = 3

# Addition
print("my_age + operand =", my_age + operand)

# Subtraction
print("my_age - operand =", my_age - operand)

# Multiplication
print("my_age * operand =", my_age * operand)

# True division
print("my_age / operand =", my_age / operand)

# Floor division
print("my_age // operand =", my_age // operand)

# Remainder
print("my_age % operand =", my_age % operand)

# Exponentiation
print("my_age ** operand =", my_age ** operand)
